package com.apirest.efi.models.entity;

import java.io.Serializable;
import java.util.List;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.NotNull;
import javax.validation.constraints.Size;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlTransient;
import org.codehaus.jackson.annotate.JsonIgnore;

/**
 *
 * @author Ricardo
 */
@Entity
@Table(name = "categorias_pregunta")
@XmlRootElement
@NamedQueries({
    @NamedQuery(name = "CategoriasPregunta.findAll", query = "SELECT c FROM CategoriasPregunta c")
    , @NamedQuery(name = "CategoriasPregunta.findById", query = "SELECT c FROM CategoriasPregunta c WHERE c.id = :id")
    , @NamedQuery(name = "CategoriasPregunta.findByNombre", query = "SELECT c FROM CategoriasPregunta c WHERE c.nombre = :nombre")})
public class CategoriasPregunta implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @NotNull
    @Column(name = "id")
    private Long id;
    @Basic(optional = false)
    @NotNull
    @Size(min = 1, max = 255)
    @Column(name = "nombre")
    private String nombre;
    @OneToMany(cascade = CascadeType.ALL, mappedBy = "idCategoria")
    private List<Preguntas> preguntasList;

    public CategoriasPregunta() {
    }

    public CategoriasPregunta(Long id) {
        this.id = id;
    }

    public CategoriasPregunta(Long id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @XmlTransient
    @JsonIgnore
    public List<Preguntas> getPreguntasList() {
        return preguntasList;
    }

    public void setPreguntasList(List<Preguntas> preguntasList) {
        this.preguntasList = preguntasList;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof CategoriasPregunta)) {
            return false;
        }
        CategoriasPregunta other = (CategoriasPregunta) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.apirest.efi.models.entity.CategoriasPregunta[ id=" + id + " ]";
    }
    
}
